// creating animals 
// Using factory method

class lion {
    constructor() {
        this.animal
            animal = "lion"
    
    }
}

class cat {
    constructor() {
        this.animal
            animal = "cat"
    
    }
}

class AnimalFactory {
    static CreateAnimal(type){
        switch(type) {
            case animal: "lion"
                return new lion ();

            case animal: "cat"
                return new cat ();       
        }
    }
}

